import axios from 'axios';

import { type IWarehouseAssignment } from '@/shared/model/warehouse-assignment.model';

const baseApiUrl = 'api/warehouse-assignments';

export default class WarehouseAssignmentService {
  public find(id: string): Promise<IWarehouseAssignment> {
    return new Promise<IWarehouseAssignment>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: string): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IWarehouseAssignment): Promise<IWarehouseAssignment> {
    return new Promise<IWarehouseAssignment>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IWarehouseAssignment): Promise<IWarehouseAssignment> {
    return new Promise<IWarehouseAssignment>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IWarehouseAssignment): Promise<IWarehouseAssignment> {
    return new Promise<IWarehouseAssignment>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
